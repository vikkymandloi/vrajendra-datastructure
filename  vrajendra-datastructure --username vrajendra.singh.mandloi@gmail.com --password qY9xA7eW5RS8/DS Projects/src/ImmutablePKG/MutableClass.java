package ImmutablePKG;
// A mutable class 
class MutableClass{
	private String name;
	private Integer empid;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getEmpid() {
		return empid;
	}
	public void setEmpid(Integer empid) {
		this.empid = empid;
	}
}